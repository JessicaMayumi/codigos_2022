a = int ( input ( "Insira um número ( A ): "))
b = int ( input ( "Insira outro número ( B ): "))
c = int ( input ( "Insira mais um número ( C ): "))

if ( a + b) == c:
    print ( "A soma de A com B é igual ao valor C ")

elif ( a + b ) < c:
    print ( "A soma de A com B é menor que o valor C ")

else:
    print ( "A soma de A com B é maior que o valor C ")